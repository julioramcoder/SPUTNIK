#Par o impar.

numero = int(input("ingresa un número: ")) #se pide el número
if numero%2 == 0: print(f"el número {numero} es par") #si el número módulo 2 es igual a 0 (es decir, el residuo de la división es 0) el numero es par
else: print(f"el  número {numero} es impar")           #si el número no es par, entonces debe ser inpar