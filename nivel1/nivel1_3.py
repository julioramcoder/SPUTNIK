#Área del triángulo.

base = float(input("ingresa la base del triángulo: ")) #se pide al usuario que ingrese la base del triangulo
altura = float(input("ingresa la altura del triángulo: "))#se pide al usuario que ingrese la altura del triángulo

area = base*altura/2 #se calcula el área del triángulo

print(area) #se muestra el área al usuario