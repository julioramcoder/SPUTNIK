#Edad futura: pide la edad actual y muestra cuántos años tendrá el usuario dentro de 10 años.

edad = int(input("Cúal es tu edad actual?: ")) #se pide la edad del usuario
edadFutura = edad + 10                         #se calcula la edad futura
print(f"En 10 años tendrás {edadFutura} años") #se muestra el resultado al usuario